def find_available_port(start_port=8090):
    """Find an available port starting from start_port"""
    port = start_port
    while port < start_port + 100:
        try:
            test_socket = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
            test_socket.bind(("0.0.0.0", port))
            test_socket.close()
            return port
        except OSError:
            port += 1
    return start_port

